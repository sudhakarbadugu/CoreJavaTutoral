class Months{
public static void main(String args[]){
	int months=1;
	switch(months){
	case 1:{
	System.out.println("january:"+months);
	break;
	}
	case 2:{
	System.out.println("february:"+months);
	break;
	}
	case 3:{
	System.out.println("march:"+months);
	break;
	}
	default:{
	System.out.println("december:"+months);
	break;
	}
	}
	}

}