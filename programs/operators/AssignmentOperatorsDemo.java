class AssignmentOperatorsDemo {
	static public void main(String[] args){
		int x = 21;
		System.out.println(x);
		
		int y = x +1;	//22
		System.out.println(y);
		
		int	z = x + y; //43
		System.out.println(z);
		
		x += 1; //x = x+1; 
		System.out.println(x);
		
		
		y = 5; //y = y -5;
		System.out.println(y);
	}
}